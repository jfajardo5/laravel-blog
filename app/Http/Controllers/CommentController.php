    {
        $request->validate([
            'article_id' => 'numeric|exists:articles,id',
            'body' => 'string|required'
        ]);
        Comment::create([
            'article_id' => $request->input('article_id'),
            'user_id' => $request->user()->id,
            'body' =>$request->input('body')
        ]);
    }

